//method OverLoading Example

#include<iostream>
using namespace std;

class Printdata
{
	public:
	void print(int v1)
	{
		cout<<"<"<<v1<<">"<<endl;
	}

	void print(int v1,int v2)
	{
		cout<<"["<<v1<<v2<<"]"<<endl;
	}

	void print(char const *s)
	{
		cout<<" \""<<s<<"\" "<<endl; //this for writing o/p like-- "Rahul" with double quotes.
	}
};

int main()
{
	Printdata pd;
	pd.print(23,54);
	pd.print("Rahul");
	pd.print(34);

	return 0;
}
